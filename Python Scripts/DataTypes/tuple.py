# tuple.py

# tuple for immutable data
days = ("Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday")

# tuple can hold different variable types
mixed_types = ("Pipe", 100, "Galv Steel", 102.5)

print(days[3])
print(mixed_types[1], type(mixed_types[1]))


# Combining lists and tuples for more use cases

my_tuple_list = [("Alice", 53), ("Bob", 31), ("Eve", 32)]

print(my_tuple_list[0])
print(my_tuple_list[0][0])
